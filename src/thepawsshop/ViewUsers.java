    public void UpdateDB(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            sqlConn = DriverManager.getConnection(dataConn, username, password);
            pst = sqlConn.prepareStatement("select * from users");
            rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            n1 = stData.getColumnCount();
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(this, "Database Connection Error.");
        }
    }
    
    public void Show(){
        
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            sqlConn = DriverManager.getConnection(dataConn, username, password);
            pst = sqlConn.prepareCall("select * from users");
            rs = pst.executeQuery();
            
            while (rs.next()){
                
                String uID = rs.getString("User_ID");
                String uFName = rs.getString("First_Name");
                String uLName = rs.getString("Last_Name");
                String uPhone = String.valueOf(rs.getInt("Phone_No"));
                String uAddress = rs.getString("Address");
                String uEmail = rs.getString("Email");
                String uRole = rs.getString("Role");
                String uStatus = rs.getString("Status");
                String tbData[] = {uID, uFName, uLName, uPhone, uAddress, uEmail, uRole, uStatus};
                DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
                tblModel.addRow(tbData);
                
            }
            
        }
        catch(Exception ex){
            
            JOptionPane.showMessageDialog(this, "Database connection failed.");
            
        }
        
    }
    
